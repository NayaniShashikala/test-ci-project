public class Calculator {

    public float addTwoNumbers(float num1, float num2) {
        return num1 + num2;
    }

    public float subtractTwoNumbers(float num1, float num2) {
        return num1 - num2;
    }

    public float multiplyTwoNumbers(float num1, float num2) {
        return num1 * num2;
    }

    public float divideTwoNumbers(float num1, float num2) {
        return num1 / num2;
    }
}
